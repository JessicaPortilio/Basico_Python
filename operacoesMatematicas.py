# Vamos usar python como uma calculadora

# Você pode digitar uma expressão e o resultado será apresentado
# A sintaxe de expressões é usal:
# operadores +, -, * e /
# parêntes (()) podem ser usados para agrupar expressões.


print(2 + 2)

print(5 - 2)

print(5 * 2)

print(10 / 2)

print((50 - 5 * 6) / 4)

# Para fazer a divisão obter um resultado inteiro, podemos usar o operador //

print(17 / 3) # divisão clássica retornar um float
print(17 // 3) # divisão descartar a parte francionária

# para calcular o restante você pode usar %

# 17 / 3
#      5
# 15
#  2

print(17 % 3)

# Com python é possível usar o operador ** para calcular potências

print(5 ** 3) #5 * 5 = 25 * 5 = 125

# O sinal de igual ('=') é usado para atribuir um valor a uma variavel.

numero1 = 10
numero2 = 5

soma = numero1 + numero2

print(soma)

valor = 0

print(valor)

nome = ''

print(nome)




